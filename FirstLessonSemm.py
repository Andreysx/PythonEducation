while True:
    i = (input('в какой вагон сел Витя? '))
    j = (input('Номер вагона '))
    print(i.isdigit())
    print(j.isdigit())
    if i.isdigit() == False or j.isdigit() == False:
        print('неверный тип')
    else:
        break

if j == i:
    print('Определить невозможно')
else:
    r = int(i) + int(j) - 1
print('Количество вагонов в элекричке', r)

